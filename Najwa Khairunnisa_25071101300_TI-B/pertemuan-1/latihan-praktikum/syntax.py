#2) Mencetak Nama, NIM, dan Prodi
print ("Nama: Najwa Khairunnisa")
print ("NIM: 25071101300")
print ("Prodi: S1 Teknik Informatika-B")

#3) Mencetak Nama, NIM, dan Prodi menggunakan variabel
nama = "Najwa Khairunnisa"
nim = "25071101300"
prodi = "S1 Teknik Informatika-B"
print ("Nama:", nama)
print ("NIM:", nim)
print ("Prodi:", prodi)

#4) Menggabungkan 2 perintah dalam satu baris menggunakan ;
