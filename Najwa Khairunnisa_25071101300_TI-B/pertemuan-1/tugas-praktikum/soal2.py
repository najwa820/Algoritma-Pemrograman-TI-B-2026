'''
Buat sebuah program Python yang:
1. Menyimpan dua bilangan ke dalam dua variabel
2. Menghitung hasil penjumlahan dan perkalian dari kedua bilangan tersebut
3. Menampilkan hasilnya ke layar
Contoh keluaran:
Hasil penjumlahan = 15
Hasil perkalian = 50
'''

a = 5       # Menyimpan bilangan ke dalam variabel
b = 10
jumlah = a + b      # Manghitung hasil dari kedua bilangan tersebut
kali = a * b
print (f'Hasil Penjumlahan = {jumlah}')     # Menampilkan hasilnya ke layar
print (f'Hasil perkalian = {kali}')